class ForEach {
    public static void main(String[] args) {
        int a[] = { 10, 20, 30, 40, 50, 60 };
        for (int b : a) {
            System.out.println(b);
            System.out.println("timi");
        }

        for (int i = 0; i <= 4; i++) {
            System.out.println(a[i]);
        }
    }
}