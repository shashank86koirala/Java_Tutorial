class StaticArray {
    public static void main(String[] args) {
        int a[] = new int[6];
        a[0] = 10;
        a[1] = 20;
        a[2] = 40;
        a[3] = 50;
        a[4] = 30;
        a[5] = 60;
        for (int i = 0; i <= 5; i++) {
            System.out.println(a[i]);
        }
    }
}